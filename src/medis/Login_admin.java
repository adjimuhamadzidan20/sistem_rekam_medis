    private void cekValidasi(String user, String pass) {
        String sql = "SELECT * FROM tb_admin WHERE username = ? AND password = ?";
        PreparedStatement stat;
        Connection conn;
        
        try {
            conn = koneksi_db.sambung_db();
            stat = conn.prepareStatement(sql);
            stat.setString(1, user);
            stat.setString(2, pass);
            
            ResultSet hasil = stat.executeQuery();
            if (hasil.next()) {
                new Menu_Utama().setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Username atau password tidak sesuai!", "Informasi", JOptionPane.ERROR_MESSAGE);
                username.setText("");
                password.setText("");
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }   
    }
    
    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        inputUser = username.getText();
        inputPass = password.getText();
        
        if(inputUser.equals("") || inputPass.equals("")) {
            JOptionPane.showMessageDialog(null, "Mohon masukan username dan password", "Informasi", JOptionPane.WARNING_MESSAGE);
            username.setText("");
            password.setText("");
        }
        else {
            cekValidasi(inputUser,inputPass);   
        } 
    }//GEN-LAST:event_loginActionPerformed

